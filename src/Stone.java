public class Stone {
    private int y;
    private int x;
    private String color;

    public Stone(int y, int x, String color) {
        this.y = y;
        this.x = x;
        this.color = color;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }

    public String getColor() {
        return color;
    }
}